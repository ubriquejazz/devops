#!/bin/bash

Modificar el ejercicio anterior para que antes de crear el fichero 
compruebe que no exista. En caso de que exista avisará del hecho 
por pantalla y creará el fichero pero añadiéndole un 1 al final del 
nombre (aguado1, por ejemplo). 

Si también existe un fichero con ese nombre, lo creará con un 2 al 
final del nombre, así seguiremos hasta intentar el 9. Si también existe 
un fichero con 9 al final del nombre, avisará del hecho y no creará nada.
