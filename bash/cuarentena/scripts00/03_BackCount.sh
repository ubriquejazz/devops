#!/bin/bash

Crear un script llamado cuenta_atras.sh que nos pida dos números 
cualesquiera, deberá mostrar por pantalla todos los números entre 
el mayor de los dos introducidos y el menor. 

 si introducimos 20 1 nos mostrará los números del 20 al 1
 si introducimos 56 89 nos mostrará los números del 89 al 56
