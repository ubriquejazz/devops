#!/bin/bash

Hacer un script que acepte como parámetro un número: 7
El script debe avisar por pantalla si no se le pasa ningún 
parámetro, o si el parámetro que se le pasa no es un número. 
Una vez comprobado que le ha pasado un número, dibujara por 
pantalla tantos caracteres @ como indique el número.
