#!/bin/bash

Hacer un script que admita como parámetros el nombre de dos ficheros. 
El script debe avisar si los parámetros pasados no existen como ficheros, 
o si no se le pasan parámetros, o si solo se pasa un parámetro. 
Una vez comprobados que se le han pasado dos ficheros, el script debe 
mostrar por pantalla el tamaño en KB de cada uno de ellos.
