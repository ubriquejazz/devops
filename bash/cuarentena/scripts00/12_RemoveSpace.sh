quita_blancos.sh (12)
Este script debe automáticamente, renombrar todos los ficheros del directorio actual 
de modo que se cambien todos los espacios en blanco de los nombres de los ficheros 
por subrayados bajos (el carácter _ ). Así, si en el directorio indicado como parámetro 
hay un fichero como Mi carta de amor al ejecutar el script cambiará su nombre por 
Mi_carta_de_amor. Esto debe hacerse automáticamente para todos los ficheros del directorio 
actual que tengan espacios en blanco en el nombre.
