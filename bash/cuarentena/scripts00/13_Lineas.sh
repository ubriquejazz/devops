Script que aceptará tres parámetros, el primero será un carácter cualquiera, 
el segundo un número entre 1 y 60 y el tercero un número entre 1 y 10. El script 
debe dibujar por pantalla tantas líneas como indique el parámetro 3, cada línea 
formada por tantos caracteres del tipo parámetro 1 como indique el número indicado 
en parámetro 2. El script debe controlar que no se le pase alguno de los parámetros 
y que los números no estén comprendidos entre los límites indicados. 

Ejemplo: ./líneas.sh k 20 5 (escribirá 5 líneas, cada una de ellas formadas por 20 letras k.
