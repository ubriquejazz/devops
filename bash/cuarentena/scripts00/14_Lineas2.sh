Crear un fichero con nombre palabra.txt y escribir en el mismo una palabra en una única línea. 
Modificar el script anterior, de modo que no se le pase el carácter. En su lugar se usará la 
palabra leída de dicho fichero palabra.txt. 

Ejemplo: ./lineas2.sh 6 5  (escribirá 5 líneas, 
cada una de ellas formadas por repetir 6 veces la palabra que este escrita en palabra.txt).