#!/bin/bash

Crear un script llamado listan.sh. La primera parte del ejercicio es como la del 16
(media.sh), pero una vez introducidos todos los números queremos verlos listados 
por pantalla, en el mismo orden en que se han ido introduciendo. Para realizar 
este ejercicio no esta permitido utilizar ficheros auxiliares.

-- 18)

Script llamado ascii.sh. El código ascii de la letra a mayúscula (A) es el 65, 
el de la B es el 66, el de la C es el 67… así hasta el de la Z que es el 90. 
Queremos que nuestro script nos pida por pantalla un número del 65 al 90 y nos 
escriba la letra a la que pertenece dicho código. 
El script no puede tener más de 20 líneas.

-- 19)

Cread un fichero palabritas.txt e introducid dentro una serie de palabras, 
una por cada línea. Cread un script llamado ahorcado.sh. Queremos que ese 
programa elija una palabra al azar de las existentes en el fichero y nos 
permita jugar al ahorcado con dicha palabra. No hace falta pintar ninguna 
figura, basta con ir escribiendo la palabra por pantalla. https://es.wikipedia.org/wiki/Ahorcado_(juego)

-- 20)

Cread un script llamado frecuenciaf.sh que automáticamente 
lea y procese todos los ficheros con extensión txt del directorio actual. 
Por pantalla nos indicar cual es la letra que más veces aparece 
en los ficheros (por ejemplo, letra A que aparece 1023 veces) 
Nos da igual que la letra este en mayúsculas, en minúsculas o con tilde. (A a á Á es la misma letra). 
Queremos ver la letra que más veces aparece, la segunda y la tercera.
