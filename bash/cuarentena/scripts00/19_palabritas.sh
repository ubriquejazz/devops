Cread un script llamado frecuenciaf.sh que automáticamente 
lea y procese todos los ficheros con extensión txt del directorio actual. 
Por pantalla nos indicar cual es la letra que más veces aparece 
en los ficheros (por ejemplo, letra A que aparece 1023 veces) 
Nos da igual que la letra este en mayúsculas, en minúsculas o con tilde. (A a á Á es la misma letra). 
Queremos ver la letra que más veces aparece, la segunda y la tercera.
